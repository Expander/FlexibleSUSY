bas1 = - as^2 (11 - 2/3 nf)/4;

bas2 = - as^3 (102 - 38/3 nf)/4^2;

bas3 = - as^4 (2857/2 - 5033/18 nf + 325/54 nf^2)/4^3;

bas4 = - as^5 (
    + 149753/6 + 3564 Zeta[3]
    - (1078361/162 + 6508/27 Zeta[3]) nf
    + (50065/162 + 6472/81 Zeta[3]) nf^2
    + 1093/729 nf^3
