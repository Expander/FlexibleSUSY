bas5 = - as^6 (
    - (8157455/16 + 621885/2*Zeta[3] - 88209/2*Zeta[4] - 288090*Zeta[5])
    - (-336460813/1944 - 4811164/81*Zeta[3] + 33935/6*Zeta[4] + 1358995/27*Zeta[5])*nf
